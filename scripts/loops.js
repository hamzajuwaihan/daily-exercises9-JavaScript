console.log("WHILE LOOP");

let x = 0;
while (x < 10) {
    console.log("VALUE OF COUNTER:" + x)
    console.log("WE STARTED WHILE ROUND #" + (x))

    //condition update
    console.log(x++)
}
//Reset counter
x = 0;
